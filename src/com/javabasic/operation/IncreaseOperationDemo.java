package com.javabasic.operation;

public class IncreaseOperationDemo {

	public static void main(String[] args) {

		// A simple example for explain how to use increment operator
		int i = 2;
		System.out.println("The value of i before perform: "+ i);
		System.out.println("The value of i when perform i++: " + i++);
		System.out.println("The value of i when perform ++i: "+ ++i);
		
	}

}
